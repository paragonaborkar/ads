package com.netapp.ads.hhcc.vo;

public class ExportsAndHost {

	String storageNodeName;
	String internalVolumeId;
	String id;
	String fileshareId;
	String storageid;
	String identifier;
	String name;
	String protocol;
	String ipinterfaces;

	public String getStorageNodeName() {
		return storageNodeName;
	}

	public void setStorageNodeName(String storageNodeName) {
		this.storageNodeName = storageNodeName;
	}

	public String getInternalVolumeId() {
		return internalVolumeId;
	}

	public void setInternalVolumeId(String internalVolumeId) {
		this.internalVolumeId = internalVolumeId;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getFileshareId() {
		return fileshareId;
	}

	public void setFileshareId(String fileshareId) {
		this.fileshareId = fileshareId;
	}

	public String getStorageid() {
		return storageid;
	}

	public void setStorageid(String storageid) {
		this.storageid = storageid;
	}

	public String getIdentifier() {
		return identifier;
	}

	public void setIdentifier(String identifier) {
		this.identifier = identifier;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getProtocol() {
		return protocol;
	}

	public void setProtocol(String protocol) {
		this.protocol = protocol;
	}

	public String getIpinterfaces() {
		return ipinterfaces;
	}

	public void setIpinterfaces(String ipinterfaces) {
		this.ipinterfaces = ipinterfaces;
	}

}
